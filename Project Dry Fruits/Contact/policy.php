<b>Mục đích và phạm vi thu thập</b>

Việc thu thập dữ liệu chủ yếu trên website bao gồm: email, điện thoại, tên đăng nhập, mật khẩu đăng nhập, địa chỉ khách hàng thành viên). Đây là các thông tin website cần thành viên cung cấp bắt buộc khi đăng ký sử dụng dịch vụ và để website liên hệ xác nhận khi khách hàng đăng ký sử dụng dịch vụ trên website nhằm đảm bảo quyền lợi cho cho người tiêu dùng.<br><br>

Trong quá trình giao dịch thanh toán tại website, chúng tôi chỉ lưu giữ thông tin chi tiết về đơn hàng đã thanh toán của thành viên, các thông tin về số tài khoản ngân hàng của thành viên sẽ không được lưu giữ.<br><br>

Các thành viên sẽ tự chịu trách nhiệm về bảo mật và lưu giữ mọi hoạt động sử dụng dịch vụ dưới tên đăng ký, mật khẩu và hộp thư điện tử của mình. Ngoài ra, thành viên có trách nhiệm thông báo kịp thời cho webiste hoaquasay.vn về những hành vi sử dụng trái phép, lạm dụng, vi phạm bảo mật, lưu giữ tên đăng ký và mật khẩu của bên thứ ba để có biện pháp giải quyết phù hợp.<br><br>

<b>Phạm vi sử dụng thông tin</b><br><br>

Công ty sử dụng thông tin thành viên cung cấp để:<br><br>

Cung cấp các dịch vụ đến thành viên;<br>
Gửi các thông báo về các hoạt động trao đổi thông tin giữa thành viên và website;<br>
Ngăn ngừa các hoạt động phá hủy tài khoản người dùng của thành viên hoặc các hoạt động giả mạo thành viên;<br>
Liên lạc và giải quyết với thành viên trong những trường hợp đặc biệt.<br>
Không sử dụng thông tin cá nhân của thành viên ngoài mục đích xác nhận và liên hệ có liên quan đến giao dịch tại website.<br>
Trong trường hợp có yêu cầu của pháp luật: Công ty có trách nhiệm hợp tác cung cấp thông tin cá nhân thành viên khi có yêu cầu từ cơ quan tư pháp bao gồm: Viện kiểm sát, tòa án, cơ quan công an điều tra liên quan đến hành vi vi phạm pháp luật nào đó của khách hàng. Ngoài ra, không ai có quyền xâm phạm vào thông tin cá nhân của thành viên.<br><br>

